        public string GetNextBatchID(string KaizenPublicKey)
        {
            if (string.IsNullOrEmpty(KaizenPublicKey)) return "";
            if (KaizenPublicKey.Trim() == "undefined") return "";
            Kaizen.Configuration.KaizenSession KaizenUser = UserServices.GetKaizenSession(KaizenPublicKey);
            if (KaizenUser == null) return "";
            if (!User.Identity.IsAuthenticated) return "";
            CM10100Services service = new CM10100Services(KaizenUser);
            return service.GetNextBatchID();
